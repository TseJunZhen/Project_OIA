$(function() {
  var b = $("#button_1");
  var w = $("#wrapper_1");
  var l = $("#list_1");

  w.height(0);
  b.click(function() {

    if(w.hasClass('open_1')) {
      w.removeClass('open_1');
      w.height(0);
    } else {
      w.addClass('open_1');
      w.height(l.outerHeight(true));
    }

  });
});

